public class App {
    public static void main(String[] args) {
        System.out.println("================");
        System.out.println("    Do While    ");
        System.out.println("================");

        int nilaiAwal = 10;

        do {
            System.out.println("Loop di jalankan untuk pertama kali: " + nilaiAwal);
        } while (nilaiAwal > 20);
    }
}
